    public static String TEXT_HTML = "text/html";

    public static String getMimeType(String fileName) {
        String ext = fileName.substring(fileName.lastIndexOf('.') + 1);
        switch (ext) {
            case "3dm":
                return THREE_DM;
            case "css":
                return CSS;
            case "html":
            default:
                return TEXT_HTML;
        }
    }

