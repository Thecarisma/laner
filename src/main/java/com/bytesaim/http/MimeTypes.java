    public static String getMimeType(String fileName) {
        String ext = fileName.substring(fileName.lastIndexOf('.') + 1).toLowerCase();
        switch (ext) {
            case "3dm":
                return THREE_DM;
            case "mp4":
                return MP4;
            case "wmv":
                return WMV;
            case "mkv":
                return MKV;
            case "zip":
                return ZIP;
            case "mp3":
                return MP3;
            case "jpg":
                return JPG;
            case "png":
                return PNG;
            case "css":
                return CSS;
            case "js":
                return JS;
            case "html":
            default:
                return TEXT_HTML;
        }
    }

